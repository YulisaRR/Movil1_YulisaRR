@Preview
@Composable
fun DiceRollerApp(){
    DiceWithButtonAndImage(modifier = Modifier
        .fillMaxSize()
        .wrapContentSize(Alignment.Center))
}

private fun getRandomDiceImage() : Int{
    return (1..6).random()
}

@Preview(showBackground = true)
@Composable
fun DiceWithButtonAndImage(modifier: Modifier = Modifier){
    var res by remember { mutableStateOf( 1) }
    var mut by remember { mutableStateOf( 1) }
    val imageResource = when(res) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
    val imageResource1 = when(mut) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
    Column(modifier = modifier, horizontalAlignment = Alignment.CenterHorizontally)
    {
        Image(painter = painterResource(imageResource), contentDescription = res.toString(), )
        Image(painter = painterResource(imageResource1), contentDescription = mut.toString())
        Spacer(modifier = Modifier.height(16.dp))
        Button(onClick = {
            res = getRandomDiceImage()
            mut = getRandomDiceImage()
        }) {
            Text(stringResource(R.string.roll), fontSize = 24.sp)
        }
    }

}